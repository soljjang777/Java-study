package com.mz.hc;

public class Remain {
	private int e;
	private int s;
	private int r;
	
	public Remain() {
		
	}

	public Remain(int e, int s, int r) {
		super();
		this.e = e;
		this.s = s;
		this.r = r;
	}

	public int getE() {
		return e;
	}

	public void setE(int e) {
		this.e = e;
	}

	public int getS() {
		return s;
	}

	public void setS(int s) {
		this.s = s;
	}

	public int getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}

}
